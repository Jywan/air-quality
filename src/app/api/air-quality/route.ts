async function fetchSidoItems(sido: string, pageCount: number): Promise<Record<string, string>[]> {
    const fetchPage = (pageNo: number) => {
        const params = new URLSearchParams({
            serviceKey: process.env.AIRKOREA_API_KEY!,
            returnType: "json",
            numOfRow: "10",
            pageNo: String(pageNo),
            sidoName: sido,
            ver: "1.0",
        });
        return fetch(`${BASE_URL}/getCtprvnRltmMesureDnsty?${params}`, {
            next: { revalidate: 1800 },
        }).then((r) => r.json());
    };
    const pages = await Promise.all(Array.from({ length: pageCount }, (_, i) => fetchPage(i + 1)));
    return pages.flatMap((p) => p.response.body.items ?? []);
}

async function fetchNationwide() {
    const results = await Promise.all(
        ALL_SIDOS.map((s) => fetchSidoItems(s, Math.min(PAGE_COUNT[s] ?? 2, 2)))
    );
    const now = new Date();
    const data = ALL_SIDOS.map((sidoName, i) => {
        const items = results[i];
        const stationToDistrict = STATION_MAP[sidoName] ?? {};
        const pm25: number[] = [], pm10: number[] = [], o3: number[] = [], no2: number[] = [], co: number[] = [], so2: number[] = [];
        for (const item of items) {
            if (!stationToDistrict[item.stationName]) continue;
            const v_pm25 = Number(item.pm25Value); if (!isNaN(v_pm25)) pm25.push(v_pm25);
            const v_pm10 = Number(item.pm10Value); if (!isNaN(v_pm10)) pm10.push(v_pm10);
            const v_o3   = Number(item.o3Value);   if (!isNaN(v_o3))   o3.push(v_o3);
            const v_no2  = Number(item.no2Value);  if (!isNaN(v_no2))  no2.push(v_no2);
            const v_co   = Number(item.coValue);   if (!isNaN(v_co))   co.push(v_co);
            const v_so2  = Number(item.so2Value);  if (!isNaN(v_so2))  so2.push(v_so2);
        }
        const avg = (arr: number[]) => arr.length ? arr.reduce((a, b) => a + b, 0) / arr.length : 0;
        return {
            districtName: sidoName,
            pm25: Math.round(avg(pm25)),
            pm10: Math.round(avg(pm10)),
            o3:  parseFloat(avg(o3).toFixed(3)),
            no2: parseFloat(avg(no2).toFixed(3)),
            co:  parseFloat(avg(co).toFixed(2)),
            so2: parseFloat(avg(so2).toFixed(3)),
            updatedAt: now.toISOString(),
            hourly: [],
            noData: pm25.length === 0 && pm10.length === 0,
        };
    });
    return NextResponse.json({ data, isMock: false, unmapped: [] });
}

export async function GET(request: NextRequest) {

    const sido = request.nextUrl.searchParams.get("sido") ?? "서울";

    if (!process.env.AIRKOREA_API_KEY) {
        return NextResponse.json({ data: generateMockData(), isMock: true });
    }

    try {
        if (sido === "전국") return await fetchNationwide();

        const pageCount = PAGE_COUNT[sido] ?? 4;
        const fetchPage = (pageNo: number) => {
            const params = new URLSearchParams({
                serviceKey: process.env.AIRKOREA_API_KEY!,
                returnType: "json",
                numOfRow: "10",
                pageNo: String(pageNo),
                sidoName: sido,
                ver: "1.0",
            });
            return fetch(`${BASE_URL}/getCtprvnRltmMesureDnsty?${params}`, {
                next: { revalidate: 1800 },
            }).then((r) => r.json());
        };

        const pages = await Promise.all(
            Array.from({ length: pageCount }, (_, i) => fetchPage(i + 1))
        );
        const items: Record<string, string>[] = pages.flatMap(
            (p) => p.response.body.items ?? []
        );

        const stationToDistrict = STATION_MAP[sido] ?? {};

        type Acc = { pm25: number[]; pm10: number[]; o3: number[]; no2: number[]; co: number[]; so2: number[]; noData: boolean };
        const districtMap = new Map<string, Acc>();

        const unmapped = new Set<string>();
        for (const item of items) {
            const key = stationToDistrict[item.stationName];
            if (!key) { unmapped.add(item.stationName); continue; }
            if (!districtMap.has(key)) districtMap.set(key, { pm25: [], pm10: [], o3: [], no2: [], co: [], so2: [], noData: false });
            const d = districtMap.get(key)!;
            const pm25 = Number(item.pm25Value);
            const pm10 = Number(item.pm10Value);
            const o3   = Number(item.o3Value);
            const no2  = Number(item.no2Value);
            const co   = Number(item.coValue);
            const so2  = Number(item.so2Value);
            if (!isNaN(pm25)) d.pm25.push(pm25);
            if (!isNaN(pm10)) d.pm10.push(pm10);
            if (!isNaN(o3))   d.o3.push(o3);
            if (!isNaN(no2))  d.no2.push(no2);
            if (!isNaN(co))   d.co.push(co);
            if (!isNaN(so2))  d.so2.push(so2);
            if (isNaN(pm25) && isNaN(pm10) && isNaN(o3)) d.noData = true;
        }

        const now = new Date();
        const result = Array.from(districtMap.entries()).map(([name, v]) => ({
            districtName: name,
            pm25: v.pm25.length ? Math.round(v.pm25.reduce((a, b) => a + b, 0) / v.pm25.length) : 0,
            pm10: v.pm10.length ? Math.round(v.pm10.reduce((a, b) => a + b, 0) / v.pm10.length) : 0,
            o3:  v.o3.length  ? parseFloat((v.o3.reduce((a, b) => a + b, 0)  / v.o3.length).toFixed(3))  : 0,
            no2: v.no2.length ? parseFloat((v.no2.reduce((a, b) => a + b, 0) / v.no2.length).toFixed(3)) : 0,
            co:  v.co.length  ? parseFloat((v.co.reduce((a, b) => a + b, 0)  / v.co.length).toFixed(2))  : 0,
            so2: v.so2.length ? parseFloat((v.so2.reduce((a, b) => a + b, 0) / v.so2.length).toFixed(3)) : 0,
            updatedAt: now.toISOString(),
            hourly: [],
            noData: v.noData && v.pm25.length === 0,
        }));

        return NextResponse.json({ data: result, isMock: false, unmapped: [...unmapped] });
    } catch {
        return NextResponse.json({ data: generateMockData(), isMock: true });
    }
}